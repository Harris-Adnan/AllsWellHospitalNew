    public ConsultantTreatPatient(ConsultantDoctor consultantIn, Patient patientIn) {
        loggedInConsultant = consultantIn;
        selectedPatient = patientIn;
        
        DBManager db = new DBManager();
        drugs = db.loadDrugs();
        patientsDrugs = db.loadPatientsDrugs();
        
        initComponents();
        //sets the text lable with patients name and number
        lblPatient.setText(selectedPatient.toString());
        
        //This part of the code fills the Listbox with drugs to select
        DefaultListModel model = new DefaultListModel();

         for(Map.Entry<Integer,Drug> drugEntry : drugs.entrySet())
        {
            Drug actualDrug = drugEntry.getValue();
                model.addElement(actualDrug);
        }
        lstDrugs.setModel(model);
        
        
        //This part of the code fills the Listbox with patients drugs
        

         for(Map.Entry<Integer,PatientsDrugs> patientsDrugEntry : patientsDrugs.entrySet())
        {
            int tempPatientId = patientsDrugEntry.getValue().getPatientId();
            int patientId = selectedPatient.getPatientNo();
            PatientsDrugs actualPatientsDrug = patientsDrugEntry.getValue();
            if(patientId == tempPatientId)
            {
                
                model2.addElement(actualPatientsDrug);
            }
            
        }
        lstPatientsDrugs.setModel(model2);
        
        
    }

