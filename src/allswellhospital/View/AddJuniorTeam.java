    public AddJuniorTeam(ConsultantDoctor consultantIn, JuniorDoctor juniorIn) {
        
        loggedInConsultant = consultantIn;
        selectedJunior = juniorIn;
        DBManager db = new DBManager();
        teams = db.loadTeams();
        
        initComponents();
        
        //This part of the code fills the Listbox with teams to select
        DefaultListModel model = new DefaultListModel();

         for(Map.Entry<Integer,TeamOfDoctors> teamEntry : teams.entrySet())
        {
            TeamOfDoctors actualTeam = teamEntry.getValue();
                model.addElement(actualTeam);
        }
        lstTeams.setModel(model);
        
        
        //On the page Load the text boxes are autofilled to the saved details of the selected Junior Doctor
        txtFirstName.setText(selectedJunior.getFirstname());
        txtFirstName.disable();
        txtLastName.setText(selectedJunior.getLastname());
        txtLastName.disable();
        txtGrade.setText(selectedJunior.getGrade());
        txtGrade.disable();
    }

