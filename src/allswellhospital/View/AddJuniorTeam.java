    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed

        // this checks to see if a Ward has been selected
        if(lstTeams.getSelectedIndex() != -1)
        {
            String teamCode = selectedTeam.getTeamCode();
            String juniorId = selectedJunior.getJuniorId();

            //the junior doctors details are set
            JuniorDoctor newJuniorTeam = new JuniorDoctor( teamCode, juniorId);
            DBManager db = new DBManager();
            //DB managers method is called (update Junior Team)
            db.updateJuniorTeam(newJuniorTeam);
            lblMessage.setText("New Junior Doctor Team Updated Successfully");
        }
        else
        {
            lblMessage.setText("Please Select A Team To Assign To The Junior Doctor!");
        }
    }//GEN-LAST:event_btnConfirmActionPerformed

