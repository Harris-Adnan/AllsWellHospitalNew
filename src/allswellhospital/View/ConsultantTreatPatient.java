    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // this checks to see if a ward has been selected
        if(lstDrugs.getSelectedIndex() != -1)
        {     
            int drugId = selectedDrug.getDrugId();
            int patientId = selectedPatient.getPatientNo();
            String drugName = selectedDrug.getDrugName();
            String dosage = selectedDrug.getDosage();
            double price = selectedDrug.getPrice();
            
             
            
                //the patients details are set
                PatientsDrugs newPatientsDrug = new PatientsDrugs( patientId, drugId, drugName, dosage, price );
                DBManager db = new DBManager();
                //DB managers method is called (admit patient)
                db.addPatientsDrug(newPatientsDrug);
                lblMessage.setText("New Patients Drug Added Successfully");
                
                //This part of the code fills the Listbox with patients Drugs to select
        //This part of the code fills the Listbox with patients drugs
        
         model2.addElement(newPatientsDrug);

         
        lstPatientsDrugs.setModel(model2);
             
            
        }
        else
        {
            lblMessage.setText("Please select a Drug to add!");
        }
        
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeletePatientsDrugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletePatientsDrugActionPerformed
        //it enures a drug is selected
        if(lstPatientsDrugs.getSelectedIndex() != -1)
        {
            //it selects drug then calls a funtion in DBManager to delete the drug
            String drugName = selectedPatientsDrug.toString();
            DBManager db = new DBManager();
            db.deletePatientsDrug(selectedPatientsDrug);
            patientsDrugs.remove(selectedPatientsDrug.getDrugLineId());
            
            
            //This part of the code fills the Listbox with drugs to select
        

            model2.removeElement(selectedPatientsDrug);
         
        lstPatientsDrugs.setModel(model2);
                    //confirmation that the Drug is deleted
            lblMessage.setText(drugName + " has been removed");
        }
        else
        {
            lblMessage.setText("Please Select a Drug First");
        }
    }//GEN-LAST:event_btnDeletePatientsDrugActionPerformed

